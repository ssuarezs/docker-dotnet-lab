        <div class="loading">Cargando productos...</div>
      {:else if products.length === 0}
        <div class="empty">No hay productos. ¬°Agrega el primero!</div>
      {:else}
        <div class="product-grid">
          {#each products as product}
            <div class="product-card">
              <h3>{product.name}</h3>
              <p class="price">${product.price}</p>
              <small>Creado: {product.createdAt ? new Date(product.createdAt).toLocaleString() : 'Desconocido'}</small>
            </div>
          {/each}
